
public class Loops1 
{
	public static void main(String args[])
	{	
		int y = 4;
		
		while (y > 0) 
		{
			for (int i = 4; i > 0; i--) 
			{
				System.out.print("*");
			}System.out.println();
			y--;
		}
	}
}

